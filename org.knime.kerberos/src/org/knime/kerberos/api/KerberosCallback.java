package org.knime.kerberos.api;

import javax.security.auth.Subject;

/**
 * Interface for callback function that gets executed with a Kerberos authenticated context.
 *
 * @author Mareike Hoeger, KNIME GmbH, Konstanz, Germany
 * @param <T> Return type of the callback function
 */
@FunctionalInterface
public interface KerberosCallback<T> {

    /**
     * This function should be implemented by KNIME nodes that need to perform operations which require a JAAS
     * {@link Subject} with a Kerberos ticket. If this method is invoked, it can be assumed that the Kerberos login was
     * successful and a ticket has been acquired.
     *
     * <p>
     * If necessary implementations can obtain the JAAS {@link Subject} using
     * {@code Subject.getSubject(AccessController.getContext())}.
     * </p>
     *
     * @return may return an Object
     * @throws Exception When something went wrong while running this callback function.
     */
    T doAuthenticated() throws Exception;
}
